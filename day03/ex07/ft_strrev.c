char	*ft_strrev(char *str)
{
	int		len;
	int		i;
	int		tmp;

	i = 0;
	while (str[i] != '\0')
		i++;
	len = i;
	while (i > (len / 2))
	{
		tmp = str[len - i];
		str[len - i] = str[i - 1];
		str[i - 1] = tmp;
		i--;
	}
	return (str);
}
