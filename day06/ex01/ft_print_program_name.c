int		ft_putchar(char c);

int		main(int argc, char **argv)
{
	int	i;

	i = 0;
	while (argc > 0 && argv[0][i] != '\0')
	{
		ft_putchar(argv[0][i]);
		i++;
	}
	ft_putchar('\n');
	return (0);
}
