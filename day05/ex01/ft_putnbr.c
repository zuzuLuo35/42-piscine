int		ft_putchar(char c);

void	ft_putnbr(int nb)
{
	long	nb_l;

	nb_l = nb;
	if (nb_l < 0)
	{
		ft_putchar('-');
		nb_l *= (-1);
	}
	if (nb_l >= 10)
		ft_putnbr(nb_l / 10);
	ft_putchar('0' + nb_l % 10);
}
